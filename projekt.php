<BR>
<U>Jak to celé začalo</U><BR>
Počátek myšlenky na tento projekt jsou v roce 2008, kdy jsem si začal vést kompletní záznamy o svých pohybech na účtu formou jednoduchých tabulek v Excelu. V té době jsem ještě platil spoustu různých poplatků a neměl nad nimi 100% kontrolu, často ani ponětí, že existují. 

<P>
V průběhu času jsem tabulky zdokonaloval, ale v roce 2012 mi už přestaly stačit a rozhodl jsem se změnit "vývojové prostředí" na PHP + MySQL.
</P>

<P>
S MySQL příkazy jsem už nějaké zkušenosti měl dříve, ale s PHP jsem začínal od úplného začátku a stále se považuji za začátečníka. Postupně objevuji nové možnosti a dostávám nové nápady, které by nemusely být užitečné jen pro mou potřebu, a tak jsem se rozhodl pro zveřejnění na internetu.
</P>

<P>
<U>Plán vývoje - priority</U><BR>
Původně jsem měl v plánu pouze aplikaci ke správě osobních financí, ale díky náročnosti zpracování poplatků různých bank jsem přehodnotil priority a přednost dostal Srovnávač poplatků.
</P>

<P>
V základu stále počítám výhradně s bankovními službami, které sám využívám, proto je dost pravděpodobné, že tento rozsah nebude dostatečný pro každého. Pokud bude dobrý ohlas, rád přidám nové možnosti podle konkrétních žádostí a návrhů. <!--Podrobný plán vývoje sdílím <a href="https://docs.google.com/document/d/10gAX-9sgKpQ3WChMuP_BwwHV6FSia4hQndfd2e2rl7I/edit?usp=sharing" target="_blank">zde</a>.-->  
</P>

<P>
<U>Spolupráce</U><BR>
Jelikož vše vyrábím ve svém volném čase, plánů mám nad hlavu a zkušenosti s vývojem webových stránek sbírám jen opravdu velmi pomalu, uvítám jakoukoli formu nabídky spolupráce na tomto projektu. Projekt je neziskový a stále v počátcích, takže nabízím hlavně možnost uplatnění pro začínající programátory, grafiky a designéry.  
